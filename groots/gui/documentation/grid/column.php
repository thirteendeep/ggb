<div class="typography">
    <h3>Row flex options</h3>

    <p>
        <strong>
            [bp]-start<br>
            <i class="sass"></i> flex-start([bp]);
        </strong>

    </p>
    <p>
        Les objets flexibles sont regroupés à partir de la ligne de départ de l'axe principal. La marge du premier objet flexible est aligné avec la ligne de départ et ensuite les objets sont alignés selon leurs marges, collées les unes aux autres.
    </p>
    <hr>
    <p>
        <strong>
            [bp]-end<br>
            <i class="sass"></i> flex-end([bp]);
        </strong>

    </p>
    <p>
        Les objets flexibles sont regroupés vers la ligne de fin de l'axe principal. La marge du dernier objet flexible est aligné avec la ligne de fin et les autres objets sont alignés avant selon leurs marges, collées les unes aux autres.
    </p>
    <hr>
    <p>
        <strong>
            [bp]-center<br>
            <i class="sass"></i> flex-center([bp]);
        </strong>

    </p>
    <p>
        Les objets flexibles sont regroupés au centre de l'axe principal. Les éléments flexibles sont accolés les uns aux autres avec leurs marges. L'espace entre la ligne de départ et la marge du premier élément est le même que celui qu'on observe entre la ligne de fin et la marge du dernier élément.
    </p>
    <hr>
    <p>
        <strong>
            [bp]-between<br>
            <i class="sass"></i> flex-between([bp]);
        </strong>

    </p>
    <p>
        L'espace est distribué de façon équitable. Le premier objet commence au début de l'axe et le dernier finit à la fin de l'axe.
    </p>
    <hr>
    <p>
        <strong>
            [bp]-around<br>
            <i class="sass"></i> flex-around([bp]);
        </strong>

    </p>
    <p>
        Les objets flexibles sont distribués de façon équitable le long de l'axe principal afin que l'espace entre deux éléments qui se suivent soit toujours le même. L'espace vide avant le premier élément et l'espace vide après le dernier élément sont égaux et correspondent à la moitié de l'espace qu'il y aura entre chaque élément.
    </p>
    <hr>
    <p>
        <strong>
            [bp]-top<br>
            <i class="sass"></i> flex-top([bp]);
        </strong>

    </p>
    <p>
        Dans la direction de l'axe secondaire, le bord de la marge extérieure de l'élément (margin box) est justifié au début de l'axe secondaire (cross-axis).
    </p>
    <hr>
    <p>
        <strong>
            [bp]-middle<br>
            <i class="sass"></i> flex-middle([bp]);
        </strong>

    </p>
    <p>
        La marge extérieure de l'élément flex est centrée par rapport à la ligne sur l'axe secondaire. Si la dimension de l'élément (dans la direction de l'axe secondaire) est supérieure à celle du conteneur flex, l'élément débordera de la même quantité des deux côtés.
    </p>
    <hr>
    <p>
        <strong>
            [bp]-bottom<br>
            <i class="sass"></i> flex-bottom([bp]);
        </strong>

    </p>
    <p>
        Tous les éléments sont alignés de telle manière que leurs lignes de base soient alignées. L'élément qui a la plus grande distance entre le bord de sa marge extérieure (dans la direction de l'axe secondaire) et sa ligne de base est justifié sur le bord de la ligne de l'axe secondaire.
    </p>
    <hr>
    <p>
        <strong>reverse</strong><br>
        Inverse les colonnes
    </p>
</div>
