<div class="typography">
    <h3>Navigation</h3>
    <p>Groots has a build-in responsive navigation</p>
    <ul>

        <li>
            To use it, just call the SASS mixin with the right parameters :<br>
            <code>@mixin nav($nav-class, $nav-height, $nav-mobile-panel-width, $nav-breakpoint)</code>
            <ul>
                <li>$nav-class</li>
                <li>$nav-height</li>
                <li>$nav-mobile-panel-width</li>
                <li>$nav-breakpoint</li>
            </ul>
        </li>
        <li>Its behavior is directed by a few attributes : <code>gr-nav-effect</code> and <code>gr-nav-sticky</code></li>
    </ul>
</div>
