<div class="typography">
    <h3>Notifications</h3>
    <p>Le système envoie des notifications pour toutes actions effectuées y compris les erreurs.</p>
    <ul>
        <li>Sur MacOSX, les notifications passeront par le centre de notification de l'OS.</li>
        <li>Pour Linux, <code>notify-send/notify-osd</code> a besoin d'être installé pour pouvoir afficher les notifications.</li>
        <li>Sur Windows 8 et supérieur, elles utiliseront les notifications toast intégrée à l'OS.</li>
        <li>Pour les autres versions de Windows, l'installation de <a href="http://www.growlforwindows.com/gfw/" target="_blank">Growl</a> permettra d'afficher les notifications.</li>
    </ul>
    <p><small><i>Note : Les notifications sont désactivables et configurables dans le fichier de configurations <code>user.js</code>.</i></small></p>
</div>
