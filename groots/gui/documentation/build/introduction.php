    <p>Le système de build est basé sur <strong>Node</strong> et utilise <strong>Gulp</strong> comme exécuteur de tâches et <strong>Bower</strong> comme gestionnaire de dépendances.</p>
    <h5>Pourquoi un système de build?</h5>
    <p>Un système de build permet d'automatiser et de gérer une multitudes de tâches pour nous. Voici un aperçu de ce que permet le système de build inclus dans Groots.</p>
    <ul>
        <li>Convertir les fichiers SASS en fichiers CSS</li>
        <li>Optimiser les images pour un chargement plus rapide</li>
        <li>Vérifier les erreurs ainsi que nos le respect de <a href="">nos codelines</a> dans les fichiers SASS et Javascript, améliorant ainsi la qualité du code</li>
        <li>Ajouter les préfixes CSS nécessaires pour certains navigateurs (selon les navigateurs marqués comme supportés dans les configurations)</li>
        <li>Compresser et fusionner les fichiers CSS et Javascripts pour un chargement plus rapide</li>
        <li>Générer les favicons pour les différents appareils</li>
        <li>Télécharger les fonts Google si le projet en utilise</li>
    </ul>
