<div class="typography">
    <h3>Shorthand Properties</h3>
    <p>Shorthand properties are an easy <i>"to go to"</i> solution when you have to set values for several CSS properties simultaneously. Make sure to you use them sparingly because even if they take more time to code, individual properties are easier to override and read.</p>
    <ul>
        <li>Use them if you need to declare every property that are grouped under the shorthand property.</li>
        <li>If you don't need to specify each property grouped under a shorthand property, use individual properties instead.</li>
        <li><a href="http://csslint.net/" target="_blank">Lint</a> error code : <span class="lint--warning">Warning</span></li>
    </ul>
</div>
