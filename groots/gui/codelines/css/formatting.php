    <h3>Formatting</h3>
    <p>How you write your CSS is <i>very</i> important for yourself and for developers that will have to build components in the same environment as you. Here are some rules that groots applies to keep the source code clean, reusable and homogeneous :</p>
    <ul>
        <li>4 spaces for indentation (no tabs).</li>
        <li>When using multiple selectors in a rule declaration, give each selector its own line.</li>
        <li>Include a single space before the opening brace of a rule set.</li>
        <li>Each property must be on a single line.</li>
        <li>Put a space after the : character for properties.</li>
        <li>Put closing braces <code>}</code> of rule declarations on a new line.</li>
        <li>Put blank lines between rule declarations.</li>
        <li>Don't specify a measurement unit if the value of the property is <code>0</code>.</li>
        <li>Don't leave a leading <code>0</code>.</li>
        <li><a href="http://csslint.net/" target="_blank">Lint</a> error code : <span class="lint--warning">Warning</span></li>
    </ul>
