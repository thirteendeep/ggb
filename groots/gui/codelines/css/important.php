<div class="typography">
    <h3>!important</h3>
    <p>As it may seems ironic, groots does not consider this css declaration <i>important</i> for many reasons. However, there are some <i>specific</i> cases when we need this declartion so groots can :</p>
    <ul>
        <li>Overide css styles for <code>@media print</code>.</li>
        <li>Overide css styles that comes with a plugin that was not developed by groots (ex: <a href="http://parsleyjs.org/" target="_blank">Parsley</a>).</li>
        <li>Any other case is not allowed.</li>
        <li><a href="http://csslint.net/" target="_blank">Lint</a> error code : <span class="lint--warning">Warning</span></li>
    </ul>
</div>
