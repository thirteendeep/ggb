    <h3>ID Selectors</h3>
    <p>ID selectors are very performant and usefull in a JS and HTML context (binding / anchors / linking <code>label</code> with its <code>input</code>). However, you should not use them in a CSS context since that groots has a modular approach.</p>
    <ul>
        <li>Never use them for styling purposes.</li>
        <li>You can use them for anchors.</li>
        <li>You can use them to link <code>labels</code> with their <code>input</code>.</li>
        <li>You can use them to bind elements with <a  href="#c-jsIdSelectors" gr-scroll-to=".gui-nav" gr-scroll-to-bottom="1">JS / Jquery</a>.</li>
        <li><a href="http://csslint.net/" target="_blank">Lint</a> error code : <span class="lint--error">Error</span></li>
    </ul>
