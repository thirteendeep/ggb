<div class="typography">
    <h3>Comments</h3>
    <p>All the rules that we apply on <a href="#c-cssComments" gr-scroll-to=".gui-nav" gr-scroll-to-bottom="0">css comments</a>
        must be applied with scss but there are some features developers need to be aware of :</p>
    <ul>
        <li>The multiline comments are preserved in the CSS output where possible</li>
        <li>The single-line comments are removed</li>
        <li>When the first letter of a multiline comment is !, the comment will always rendered into css output even in compressed output modes.</li>
        <li><a href="http://csslint.net/" target="_blank">Lint</a> error code : <span class="lint--warning">Warning</span></li>
    </ul>
    <h4>Reference</h4>
